- (NSString*)getSerialNumber
{
    return [[NSUserDefaults standardUserDefaults] objectForKey:kSerialNumber];
}

- (void)updateSerialNumber:(NSString *)serialNumber
{
    [[NSUserDefaults standardUserDefaults] setObject:serialNumber forKey:kSerialNumber];
}

- (NSString *)getUUID
{
    NSString *uuid = [[NSUserDefaults standardUserDefaults] objectForKey:kUUID];
    if (!uuid) {
        uuid = [[NSUUID UUID] UUIDString];
        [[NSUserDefaults standardUserDefaults] setObject:uuid forKey:kUUID];
    }
    return uuid;
}

